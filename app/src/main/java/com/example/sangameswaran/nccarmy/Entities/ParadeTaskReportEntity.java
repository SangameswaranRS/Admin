public class ParadeTaskReportEntity {
    String parade;
    String drillTask;
    String theoryTask;
    String crewEff;

    public String getParade() {
        return parade;
    }

    public void setParade(String parade) {
        this.parade = parade;
    }

    public String getDrillTask() {
        return drillTask;
    }

    public void setDrillTask(String drillTask) {
        this.drillTask = drillTask;
    }

    public String getTheoryTask() {
        return theoryTask;
    }

    public void setTheoryTask(String theoryTask) {
        this.theoryTask = theoryTask;
    }

    public String getCrewEff() {
        return crewEff;
    }

    public void setCrewEff(String crewEff) {
        this.crewEff = crewEff;
    }
}
